export const getVehicleSpecs = (brand, model, year) => {
  // Default values
  let engines = ['petrol', 'diesel', 'gas', 'hybrid', 'electric'];
  let transmissions = ['manual', 'automatic', 'robot', 'variator'];

  // Basic validation
  if (!brand || !model || !year) return { engines, transmissions };

  const yearNum = parseInt(year);

  // Logic for electric cars
  const isElectricModel = 
    brand === 'Tesla' || 
    model.startsWith('ID') || 
    model.startsWith('EQ') ||
    model.includes('e-tron') ||
    model === 'I-Pace' ||
    model === 'Zoe' ||
    model === 'Leaf' ||
    model === 'Spring' ||
    model === 'Bolt' ||
    model === 'bZ4X' ||
    model === 'i3' || 
    model === 'iX' ||
    model === 'Taycan';

  if (isElectricModel) {
    return {
      engines: ['electric'],
      transmissions: ['automatic'] // EVs usually treated as automatic/single-speed
    };
  }

  // Logic based on year
  if (yearNum < 2000) {
    // Old cars: mostly petrol/diesel/gas, manual/auto
    engines = ['petrol', 'diesel', 'gas'];
    transmissions = ['manual', 'automatic'];
  } else if (yearNum < 2010) {
    // 2000-2010: + robot, + hybrid (rare)
    engines = ['petrol', 'diesel', 'gas', 'hybrid'];
    transmissions = ['manual', 'automatic', 'robot', 'variator'];
    
    // Filter out electric for this era unless specific models (very rare)
    if (model !== 'Roadster') { // Tesla Roadster 2008
       // kept hybrid
    }
  } else {
    // Modern cars
    engines = ['petrol', 'diesel', 'gas', 'hybrid', 'electric'];
    transmissions = ['manual', 'automatic', 'robot', 'variator'];
  }

  // Refine by brand/model specifics (examples)
  
  // ZAZ/Daewoo/Lanos - usually manual, simple engines
  if (['ZAZ', 'Daewoo', 'Vaz'].includes(brand)) {
    transmissions = ['manual', 'automatic']; // Auto is rare but exists on some
    engines = ['petrol', 'gas']; // Diesel is rare on Lanos
  }

  return { engines, transmissions };
};
